    private void limpaCampos(){
        tfid_venda.setText("");
        ComboProduto.selecionaIndice(-1);
        ComboCliente.selecionaIndice(-1);
        tfQuantidade.setText("");
    }
    VendasDTO cliente = new VendasDTO();
    private void preencheVenda(){
        
        if (ModoOperacao != 1){
          cliente.setId_venda(Integer.parseInt(tfid_venda.getText()) );
        }
        
        
        cliente.setFk_produto( ComboProduto.retornaItemSelecionado()  );
        cliente.setFk_cliente(ComboCliente.retornaItemSelecionado() );
        cliente.setQuantidade(Integer.parseInt(tfQuantidade.getText()) );
        
    }
    private void alteraModoOperacao( int ModoOperacao){
        this.ModoOperacao = ModoOperacao;
        if (ModoOperacao != 2){
            limpaCampos();
        }
        boolean alterando = ModoOperacao > 0;
        tfQuantidade.setEditable(alterando);
        JCBCliente.setEnabled(alterando);
        jCBProduto.setEnabled(alterando);
        btnCancelar.setEnabled(alterando);
        btnConfirmar.setEnabled(alterando);
        btnNovo.setEnabled(!alterando);
        btnAlterar.setEnabled(!alterando);
        btnApagar.setEnabled(!alterando);
        
    }
    

