    private void listaVendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaVendasMouseClicked
        if (ModoOperacao == 0){
          tfid_venda.setText( listaVendas.getValueAt(listaVendas.getSelectedRow(), 0).toString() );
          ComboProduto.selecionaItem( Integer.parseInt(listaVendas.getValueAt(listaVendas.getSelectedRow(), 1).toString()) );
          ComboCliente.selecionaItem( Integer.parseInt(listaVendas.getValueAt(listaVendas.getSelectedRow(), 2).toString()) );
          tfQuantidade.setText( listaVendas.getValueAt(listaVendas.getSelectedRow(), 3).toString() );
        }
    }//GEN-LAST:event_listaVendasMouseClicked

